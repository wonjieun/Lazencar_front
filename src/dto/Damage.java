package dto;

public class Damage {
	private String carDamageSel;
	private int carDamagePrice;
	
	public String getCarDamageSel() {
		return carDamageSel;
	}
	public void setCarDamageSel(String carDamageSel) {
		this.carDamageSel = carDamageSel;
	}
	public int getCarDamagePrice() {
		return carDamagePrice;
	}
	public void setCarDamagePrice(int carDamagePrice) {
		this.carDamagePrice = carDamagePrice;
	}
}
