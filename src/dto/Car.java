package dto;

public class Car {
	private String carNum;
	private String carName;
	private String carCategory;
	private String carOil;
	private String carCondi;
	private String carLCD;//최종 점검 날짜
	private int carPrice;
	//
	
	public String getCarNum() {
		return carNum;
	}
	public void setCarNum(String carNum) {
		this.carNum = carNum;
	}
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public String getCarCategory() {
		return carCategory;
	}
	public void setCarCategory(String carCategory) {
		this.carCategory = carCategory;
	}
	public String getCarOil() {
		return carOil;
	}
	public void setCarOil(String carOil) {
		this.carOil = carOil;
	}
	public String getCarCondi() {
		return carCondi;
	}
	public void setCarCondi(String carCondi) {
		this.carCondi = carCondi;
	}
	public String getCarLCD() {
		return carLCD;
	}
	public void setCarLCD(String carLCD) {
		this.carLCD = carLCD;
	}
	public int getCarPrice() {
		return carPrice;
	}
	public void setCarPrice(int carPrice) {
		this.carPrice = carPrice;
	}
	
	
	
	
	
}
